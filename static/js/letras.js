$(document).ready(function() {

    $("#NormalButton").click(function(){
        $("body").css("font-size","140%");
    });

    $("#BigButton").click(function(){
        $("body").css("font-size","200%");
    });

    $("#VeryBigButton").click(function(){
        $("body").css("font-size","260%");
    });
});
